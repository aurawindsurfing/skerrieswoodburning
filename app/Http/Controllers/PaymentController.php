    /**
     * makePDF.
     *
     * @param mixed $invoices
     * @return void
     */
    public function makePDF($receipts)
    {
        $pdf = \App::make('dompdf.wrapper');
        $pdf->loadView('invoices.payment_receipt', compact('receipts'));
        $id = uniqid();
        $path = '/storage/tmp/invoices/'.$id.'.pdf';
        $pdf->save(public_path($path));

        return $path;
    }
